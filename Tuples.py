# Lets see how to make use of tuples in a python program

tup = (200, 50)
print(tup)

print(tup[0])       # we can access it just like the same way we access list
print(tup[1])

# how to use for loop with tuples
for values in tup:
    print(values)

# overwriting with tuple
tup = (1000, 300, 500, 600)
print(tup)